/**
 * Ответы ассистента: сначала опциональный бэкенд (обход CORS), иначе локальный «умный» режим с учётом истории.
 *
 * Опционально в .env:
 *   VITE_AI_CHAT_URL=https://ваш-сервер.com/api/chat
 *   VITE_AI_CHAT_SYSTEM=короткий системный промпт (роль ассистента), если API принимает role: system
 * Тело POST: { "messages": [{ "role": "system"|"user"|"assistant", "content": "..." }] }
 * Ответ JSON: { "reply": "текст" } или { "choices": [{ "message": { "content": "..." } }] }
 */

export type ChatTurn = { role: "user" | "ai"; text: string };

function pick<T>(arr: readonly T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]!;
}

function firstSentence(text: string, maxLen = 120): string {
  const t = text.trim().replace(/\s+/g, " ");
  if (t.length <= maxLen) return t;
  const cut = t.slice(0, maxLen);
  const sp = cut.lastIndexOf(" ");
  return (sp > 40 ? cut.slice(0, sp) : cut).trim() + "…";
}

/** Короткое «эхо» по смыслу фразы пользователя (связность + эмпатия). */
function reflectSnippet(userText: string): string {
  const t = userText.trim();
  if (t.length < 8) return "";
  return pick([
    `Похоже, для вас важно про «${firstSentence(t, 88)}» — я рядом, давайте без спешки разберёмся.`,
    `Слышу вас: речь о «${firstSentence(t, 88)}». Расскажите чуть больше — мне важно понять, как вы это переживаете.`,
  ]);
}

/** Подсказка для .env `VITE_AI_CHAT_SYSTEM`, если хотите тот же тон при запросе к своему API. */
export const SUGGESTED_SYSTEM_PROMPT =
  "Ты — Т-бот, дружелюбный ИИ-помощник кабинета ТрассА. Умеешь знакомиться: интересуйся человеком, называй себя Т-бот, будь тёплым и эмпатичным, признавай чувства собеседника. Можешь поддержать разговор и подсказать по интерфейсу кабинета. Отвечай по-русски, лаконично, если не просят развернуть.";

async function tryBackend(messages: ChatTurn[]): Promise<string | null> {
  const url = import.meta.env.VITE_AI_CHAT_URL as string | undefined;
  if (!url || typeof fetch !== "function") return null;
  try {
    const systemRaw = (import.meta.env.VITE_AI_CHAT_SYSTEM as string | undefined)?.trim();
    const mapped = messages.map((m) => ({
      role: m.role === "ai" ? ("assistant" as const) : ("user" as const),
      content: m.text,
    }));
    const body = {
      messages: systemRaw
        ? ([{ role: "system" as const, content: systemRaw }, ...mapped] as const)
        : mapped,
    };
    const res = await fetch(url, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(body),
    });
    if (!res.ok) return null;
    const data = (await res.json()) as Record<string, unknown>;
    if (typeof data.reply === "string") return data.reply;
    const choices = data.choices as Array<{ message?: { content?: string } }> | undefined;
    const c = choices?.[0]?.message?.content;
    if (typeof c === "string") return c;
    return null;
  } catch {
    return null;
  }
}

/**
 * Локальный режим: псевдо-диалог с памятью о последних репликах, темами и эмпатией.
 */
export function localSmartReply(userText: string, historyIncludingLatestUser: ChatTurn[]): string {
  const t = userText.trim();
  const lower = t.toLowerCase();
  const prior =
    historyIncludingLatestUser[historyIncludingLatestUser.length - 1]?.role === "user"
      ? historyIncludingLatestUser.slice(0, -1)
      : historyIncludingLatestUser;
  const lastAi = [...prior].reverse().find((m) => m.role === "ai");
  const lastAiText = lastAi?.text ?? "";
  const userMsgCount = historyIncludingLatestUser.filter((m) => m.role === "user").length;
  const earlyDialog = userMsgCount <= 4;

  const cont =
    /^(да|нет|ага|угу|ок|хорошо|понятно|продолжай|дальше|ещё|а ещё|расскажи|почему|зачем|и что|не знаю|не уверен|сомневаюсь)[\s!.?]*$/i.test(
      t.trim()
    );

  if (cont && lastAiText) {
    return pick([
      `Продолжая мысль: ${firstSentence(lastAiText, 80)} — как вам такой вариант: что для вас сейчас важнее — понять детали или выбрать направление?`,
      "Понимаю. Тогда уточню: вам ближе разобраться по шагам или обсудить общую картину?",
      "Хорошо. Если сузить вопрос: что бы вы хотели получить в итоге этого разговора — совет, план действий или просто выговориться?",
      "Слышу вас. Я рядом: хотите, чтобы я больше слушала и отражала словами, или перейти к конкретным шагам?",
    ]);
  }

  if (/^(привет|здравствуй|добрый|доброе|хай|hello)/i.test(t)) {
    return pick([
      "Привет! Рада с вами познакомиться поближе. Как настроение сегодня — больше про кабинет ТрассА или хочется просто поболтать и поделиться, как день?",
      "Здравствуйте! Я Т-бот — могу и по делу подсказать, и просто быть рядом в разговоре. С чего вам сейчас теплее: про интерфейс или про настроение?",
      "Привет! Мне правда интересно, как вы себя чувствуете. Расскажите, что у вас на душе — мелочь или что-то важное?",
    ]);
  }

  if (
    /познакомим|давай(те)?\s+познаком|хочу\s+с\s+(тобой|вами)\s+познаком|рад(а)?\s+познаком|приятно\s+познаком/i.test(
      lower
    ) ||
    /как\s+к\s+тебе\s+обращ|как\s+мне\s+к\s+вам\s+обращ|как\s+тебя\s+зовут|твоё\s+имя|ваше\s+имя/i.test(
      lower
    )
  ) {
    return pick([
      "С удовольствием! Меня зовут Т-бот, я помогаю в кабинете ТрассА и люблю нормальные человеческие разговоры. Как вас называть — как вам комфортнее, по имени или на «вы»? И чем вы занимаетесь, если не секрет?",
      "Приятно познакомиться! Я Т-бот. Мне близки тёплые диалоги: могу слушать, поддержать и по кабинету подсказать. Расскажите пару слов о себе — чем живёте, что сейчас на первом плане?",
      "Давайте знакомиться без формальностей. Я — Т-бот, рядом с вами в этом чате. Что для вас важно в общении: побольше поддержки, юмора или конкретики по кнопкам и разделам?",
    ]);
  }

  if (/просто\s+хочу\s+пообщ|не\s+знаю\s+о\s+чём|о\s+чём\s+говорить|мало\s+общен|одиноко\s+в\s+чат/i.test(lower)) {
    return pick([
      "Понимаю, такое бывает — и это нормально. Можем начать с простого: как прошёл день, что порадовало или выбило из колеи? Я послушаю без оценок.",
      "Иногда сложно найти тему. Давайте мягко: что сейчас ближе — поделиться усталостью, планами на вечер или спросить что-то про кабинет? Вы выбираете темп.",
    ]);
  }

  if (/как (дела|ты|поживаешь)|что нового|как настроение/i.test(lower)) {
    return pick([
      "У меня всё ровно — я же цифровой помощник. А у вас как на душе? Если хочется выговориться — я выслушаю; если по делу — переключимся на кабинет.",
      "Спасибо, что спросили! Мне правда интересно: как вы себя чувствуете сегодня — ближе к спокойствию, усталости или чему-то ещё?",
      "Я в порядке и готова быть рядом. Расскажите по-честному, что у вас на уме — мелочь или то, о чём не с кем поделиться?",
    ]);
  }

  if (/спасибо|благодар|мерси|thanks/i.test(lower)) {
    return pick([
      "Всегда пожалуйста. Если захотите продолжить разговор или вернуться к кабинету — я здесь.",
      "Рада помочь. Не стесняйтесь писать снова — и про дела, и просто так.",
    ]);
  }

  if (/пока|до свидания|увидимся|бай\b/i.test(lower)) {
    return "До встречи! Если понадоблюсь — откройте этот чат снова. Удачи вам.";
  }

  if (/кто ты|что ты|что умеешь|расскажи о себе/i.test(lower)) {
    return pick([
      "Я Т-бот — ИИ-помощник в кабинете ТрассА: подсказываю по разделам, мероприятиям и настройкам. Но мне не всё равно, как вы себя чувствуете: могу поддержать разговор, посочувствовать, порадоваться за вас. Расскажите и о себе — чем занимаетесь, что волнует?",
      "Коротко: я Т-бот, помогаю по кабинету и умею быть рядом в обычном диалоге — без сухости. Хочу узнать и вас: что вам сейчас важнее — человеческое «как дела» или практическая подсказка по интерфейсу?",
    ]);
  }

  if (/шутк|анекдот|смешн|развлеки/i.test(lower)) {
    return pick([
      "Один программист говорит другому: «У меня баг воспроизводится стабильно». — «Это уже не баг, это фича». …Шутки шутками, а если серьёзно — чем ещё поднять настроение: музыкой, планами на вечер или маленькой целью на день?",
      "Короткая: почему календарь никогда не одинок? Потому что у него всегда полно дат. Если хотите — переключимся на что-то более серьёзное или практичное.",
    ]);
  }

  if (/устал|устала|тяжело|грустн|тоскл|одинок|тревож|страшно|боюсь/i.test(lower)) {
    return pick([
      "Слышу, что вам нелегко. Такое бывает у всех. Хотите просто выговориться — я выслушаю. Если нужен конкретный шаг: что одно маленькое действие могло бы чуть облегчить сегодняшний день?",
      "Спасибо, что делитесь. Я не заменю человека, но могу побыть рядом словом. Что сейчас отзывается сильнее — усталость, тревога или что-то другое?",
    ]);
  }

  if (/радост|счастлив|получилось|успех|класс|круто|отлично/i.test(lower)) {
    return pick([
      "Как приятно это слышать! Иногда важно остановиться и отметить удачу. Что именно вам особенно запомнилось?",
      "Здорово, что есть повод для хорошего настроения. Хотите развернуть тему или перейдём к делам в кабинете?",
    ]);
  }

  if (/скучно|не знаю чем заняться|ничего не хочется/i.test(lower)) {
    return pick([
      "Скука — тоже сигнал: иногда организм просит паузу, а иногда — новый стимул. Что ближе: отдых, маленький эксперимент или разговор ни о чём?",
      "Понимаю состояние «ни о чём». Можем придумать микро-задачу на 10 минут или просто поболтать — как вам комфортнее?",
    ]);
  }

  if (/философ|смысл жизни|зачем всё это|кто мы/i.test(lower)) {
    return pick([
      "Большой вопрос. У разных людей разные опоры: отношения, дело, творчество, вера, просто «сегодняшний день». Что для вас сейчас кажется ближе?",
      "Философия в чате — смело. Я бы сказала так: смысл часто не в ответе раз и навсегда, а в том, что мы выбираем снова и снова. О чём вы думаете чаще всего в такие моменты?",
    ]);
  }

  if (/книг|фильм|сериал|музык|хобби|игр/i.test(lower)) {
    return pick([
      "Люблю такие темы — культура и отдых заряжают. Что недавно зацепило: что-то новое или проверенное старое?",
      "Расскажите, что смотрите или слушаете в последнее время — может, порекомендуете настроение, а не жанр.",
    ]);
  }

  if (/работа|начальник|коллег|зарплат|проект|дедлайн/i.test(lower)) {
    return pick([
      "Рабочие истории бывают разные. Если не секрет: что сейчас давит сильнее — объём, люди или неопределённость?",
      "Понимаю, что рабочие темы выматывают. Иногда помогает разложить на три пункта: что под контролем, что нет, и что можно попросить у других. Хотите так разобрать?",
    ]);
  }

  if (/погод|дожд|снег|холод|жарко/i.test(lower)) {
    return "Про погоду в реальном времени я не смотрю, но могу поговорить о настроении «как погода внутри» — как оно у вас сегодня? Или вернёмся к кабинету ТрассА.";
  }

  if (/полити|выборы|войн/i.test(lower)) {
    return "Чувствительные темы лучше обсуждать с людьми, которым вы доверяете. Я могу поговорить о нейтральном: планах, книгах, работе в кабинете — что выберете?";
  }

  if (/мероприят|календар|событ/i.test(lower)) {
    return "Мероприятия в кабинете: вкладка «Мероприятия» — там календарь, создание по дате и аудитории, отмена через × у события. Ближайшие события ещё видны на главной в боковой колонке. Нужно что-то уточнить по шагам?";
  }

  if (/мессендж|личн(ое|ые) сообщ|написать коллег/i.test(lower)) {
    return "Личная переписка с коллегами — через иконку чата в шапке (рядом с профилем). Я же для общего разговора и подсказок по интерфейсу.";
  }

  if (/тем|тёмн|светл|оформлен/i.test(lower)) {
    return "Тема оформления переключается кнопкой в шапке (как «день/ночь») и сохраняется для кабинета.";
  }

  if (/профил|настрой|имя|почт|телефон/i.test(lower)) {
    return "Профиль и контакты: иконка у вашего имени в шапке → настройки. Там имя, роль, уведомления.";
  }

  if (/\?$/.test(t) || /^(почему|зачем|как|что такое|где|когда)/i.test(lower)) {
    const reflect = reflectSnippet(t);
    return pick([
      `${reflect ? reflect + " " : ""}С формальной стороны могу предложить разобрать вопрос на части: факты, предположения, что вы уже пробовали. Что для вас сейчас главная неясность?`,
      "Хороший вопрос. Если упростить: что изменится для вас, когда появится ответ? Это поможет сузить тему.",
    ]);
  }

  const reflect = reflectSnippet(t);
  const empathetic = [
    "Расскажите чуть подробнее — мне важно вас понять. Это про работу, отношения, настроение или что-то ещё?",
    "Интересно, спасибо, что делитесь. Что в этой теме для вас самое чувствительное прямо сейчас?",
    "Поняла направление. Хотите совет, мягкую поддержку или просто чтобы кто-то переформулировал вслух — без давления?",
    "Могу быть рядом в разговоре. Если удобно — задайте один уточняющий вопрос, и развернём его вместе, в вашем темпе.",
    "Я не буду оценивать — только слушать и помогать словами. Что из сказанного хочется развернуть глубже?",
  ];
  const earlyExtra = [
    "Мы только начинаем диалог — можете не спешить. Что сделает этот чат для вас полезным: знакомство, поддержка или конкретика по ТрассА?",
    "Если хотите, представьтесь в двух словах — мне приятно называть вас по-человечески, даже без настоящего имени.",
  ];
  const tail = pick([...empathetic, ...(earlyDialog ? earlyExtra : [])]);
  return pick([
    `${reflect ? reflect + "\n\n" : ""}${tail}`,
    `${reflect ? reflect + "\n\n" : ""}Я здесь и по кабинету подсказать, и просто поговорить по душам. Что откликается сильнее — практика или эмоции в теме?`,
  ]);
}

export async function getAssistantReply(history: ChatTurn[]): Promise<string> {
  const backend = await tryBackend(history);
  if (backend && backend.trim()) return backend.trim();

  const last = history[history.length - 1];
  if (!last || last.role !== "user") return "Напишите сообщение — я отвечу.";
  const text = last.text.trim();
  if (!text) return "Напишите сообщение — я отвечу.";

  return localSmartReply(text, history);
}
