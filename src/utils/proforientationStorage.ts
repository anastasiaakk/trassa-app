export function computeTagScores(answers: number[]): Record<string, number> {
  const totals = emptyScores();
  answers.forEach((optIdx, qi) => {
    const q = PROF_ORIENTATION_QUESTIONS[qi];
    if (!q?.options[optIdx]) return;
    const w = q.options[optIdx].weights;
    for (const [k, v] of Object.entries(w)) {
      if (typeof v === "number") totals[k] = (totals[k] ?? 0) + v;
    }
  });
  return totals;
}

function topTwoTags(scores: Record<string, number>): [ProfOrientationTag, ProfOrientationTag] {
  const sorted = Object.entries(scores).sort((a, b) => b[1] - a[1]);
  const a = (sorted[0]?.[0] ?? PROF_ORIENTATION_TAGS[0]) as ProfOrientationTag;
  const b = (sorted[1]?.[0] ?? PROF_ORIENTATION_TAGS[1]) as ProfOrientationTag;
  return [a, b];
}

function buildSummary(primary: string, secondary: string, kind: LearnerKindProforientation): string {
  const role = kind === "school" ? "школьного" : "студенческого";
  return `По результатам ${role} профориентационного профиля ведущее направление: «${primary}». Второе по силе: «${secondary}». Рекомендуется углублять соответствующие компетенции и знакомиться с практикой в этих зонах отрасли.`;
}

function readFile(): ResultsFile {
  try {
    const raw = localStorage.getItem(RESULTS_KEY);
    if (!raw) return { results: [] };
    const data = JSON.parse(raw) as ResultsFile;
    if (!data || !Array.isArray(data.results)) return { results: [] };
    return data;
  } catch {
    return { results: [] };
  }
}

function writeFile(file: ResultsFile) {
  localStorage.setItem(RESULTS_KEY, JSON.stringify(file));
  window.dispatchEvent(new CustomEvent("trassa-proforientation-changed"));
}

export function listProforientationResults(): ProforientationResult[] {
  return readFile().results.map((r) => ({ ...r }));
}

export function getProforientationResultForEmail(email: string): ProforientationResult | null {
  const n = email.trim().toLowerCase();
  if (!n) return null;
  return readFile().results.find((r) => r.emailNorm === n) ?? null;
}

export function saveProforientationResult(
  learnerKind: LearnerKindProforientation,
  answers: number[]
): { ok: true } | { ok: false; error: string } {
  if (answers.length !== PROF_ORIENTATION_QUESTIONS.length) {
    return { ok: false, error: "Ответьте на все вопросы теста." };
  }
  const profile = loadProfileSettings();
  const email = profile.email.trim();
  if (!email || !email.includes("@")) {
    return { ok: false, error: "Укажите e-mail в настройках профиля — без него нельзя сохранить результат." };
  }
  const emailNorm = email.toLowerCase();
  const displayName = [profile.firstName, profile.lastName].filter(Boolean).join(" ").trim() || email;
  const tagScores = computeTagScores(answers);
  const [primaryTag, secondaryTag] = topTwoTags(tagScores);
  const summary = buildSummary(primaryTag, secondaryTag, learnerKind);
  const id = `pf-${emailNorm}-${Date.now()}`;
  const completedAt = new Date().toISOString();

  const file = readFile();
  const rest = file.results.filter((r) => r.emailNorm !== emailNorm);
  rest.push({
    id,
    emailNorm,
    learnerKind,
    displayName,
    completedAt,
    answers: [...answers],
    primaryTag,
    secondaryTag,
    tagScores,
    summary,
  });
  writeFile({ results: rest });
  return { ok: true };
}

/** Совпадение с запросом подрядчика: основной или второй профиль входит в выбранные направления. */
export function resultMatchesEmployerTags(
  r: ProforientationResult,
  selectedTags: string[]
): boolean {
  if (selectedTags.length === 0) return false;
  return selectedTags.includes(r.primaryTag) || selectedTags.includes(r.secondaryTag);
}

/** Удалить сохранённые результаты профориентации по e-mail (например, при удалении пользователя администратором). */
export function deleteProforientationResultsForEmail(emailNorm: string): void {
  const n = emailNorm.trim().toLowerCase();
  if (!n) return;
  const file = readFile();
  const rest = file.results.filter((r) => r.emailNorm !== n);
  if (rest.length === file.results.length) return;
  writeFile({ results: rest });
}
